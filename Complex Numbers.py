# Complex Numbers

# Imaginary unit (i) is 'j' in Python
x = 1.04 + 3.1j 
y = 2 - 4j

print(x + y)
print(x - y)

# Getting the real and imaginary part of a complex number
print(x.real)
print(x.imag)

# Getting the conjugate complex
print(y.conjugate())

# Getting the asbolute value for a complex number (distance formula)
print(abs(y))


